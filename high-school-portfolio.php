  <p>
    Medium: Paper bag and Acrylic on Foam-core
  </p>
  <p>
    Size: 40 x 20 in.
  </p>
  <p>
    Hanged is an experimental abstract acrylic piece painted on ripped paper bags and foam board. It puts a scene in a butcher market in KunMing, China into abstract form. Using yellow acrylic paint to paste the paper bag onto the foam board instead of glue, a wrinkly vintage effect is created. I used charcoal to trace the borders and to add in shadows to create more juxtaposition in the piece. The piece transforms a gruesome scene into an abstract, mystical scene that provides room for the audience’s imagination.
  </p>

